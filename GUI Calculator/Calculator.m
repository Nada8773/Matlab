% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
 first_num=str2double(get(handles.num1,'string'));
 second_num=str2double(get(handles.num2,'string'));
 content=get(handles.choice,'String');
 choice_operation=content{get(handles.choice,'Value')};
 
 switch choice_operation
    case '+'
        sum=first_num+second_num;
    case '-'
        sum=first_num-second_num;
    case '*'
        sum=first_num*second_num;
    case '/'
        sum=first_num/second_num;
 end
 if(sum>=100)
     set(handles.result,'string',num2str(sum));
     set(handles.result,'BackgroundColor','green');
     image1=imread('im1.jpg');
     imshow(image1)
 else
     set(handles.result,'string',num2str(sum));
     set(handles.result,'BackgroundColor','red');
     image2=imread('im2.png');
     imshow(image2)
 end
